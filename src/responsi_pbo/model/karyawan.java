public class karyawan {
    private int id;
    private String nama;
    private String posisi;
    private Double kontribusi;
    private Double kedisiplinan;
    private Double kerjasama;
    private Double nilai_akhir;
    private String status;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getPosisi() {
        return posisi;
    }

    public void setPosisi(String posisi) {
        this.posisi = posisi;
    }

    public Double getKontribusi() {
        return kontribusi;
    }

    public void setKontribusi(Double kontribusi) {
        this.kontribusi = kontribusi;
    }

    public Double getKedisiplinan() {
        return kedisiplinan;
    }

    public void setKedisiplinan(Double kedisiplinan) {
        this.kedisiplinan = kedisiplinan;
    }

    public Double getKerjasama() {
        return kerjasama;
    }

    public void setKerjasama(Double kerjasama) {
        this.kerjasama = kerjasama;
    }

    public Double getNilai_akhir() {
        return nilai_akhir;
    }

    public void setNilai_akhir(Double nilai_akhir) {
        this.nilai_akhir = nilai_akhir;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    

    
}

